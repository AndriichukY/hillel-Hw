package HW5SecondTask;

public class Calculator {

    static double add(double x, double y) {
        return x + y;
    }

    static double sub(double x, double y) {
        return x - y;
    }

    static double multiply(double x, double y) {
        return x * y;
    }

    static double div (double x, double y) {
        return x / y;
    }
}
